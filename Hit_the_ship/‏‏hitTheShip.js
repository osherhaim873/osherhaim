		function mycb_change(evt) {
		
			var kombo = stage.getChildByName("comboBoxName");
			//שמירת הערך שנבחר בקומבו
			/*	console.log(evt.currentTarget.value);*/
			mychoice = evt.currentTarget.value;
			//אם נבחר הערך הראשון - "בחר נושא
			if (mychoice == 0) {
				//ניטרול כפתור "בחר
				kombo.startbtn.alpha = 0.3;
				kombo.startbtn.removeEventListener("click", startAnimation);
			} else {
				//בכל בחירה אחרת - כלומר בחר נושא תקין, נאפשר לחיצה
				kombo.startbtn.alpha = 1;
				kombo.startbtn.addEventListener("click", startAnimation);
			}
		}
		
